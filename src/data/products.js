export function findCategory(categorySlug) {
  return productCategories.find((c) => c.slug === categorySlug) || null;
}

export function findProductInCategory(category, productSlug) {
  for (const sub of category.subcategories || []) {
    const found = sub.products.find((p) => p.slug === productSlug);
    if (found) return { product: found, subcategory: sub };
  }
  const found = (category.products || []).find((p) => p.slug === productSlug);
  if (found) return { product: found, subcategory: null };
  return null;
}

export function findProduct(categorySlug, productSlug) {
  const category = findCategory(categorySlug);
  if (!category) return null;
  const result = findProductInCategory(category, productSlug);
  if (!result) return null;
  return { ...result, category };
}

export function getCategoryProducts(category) {
  const direct = category.products || [];
  const fromSubs = (category.subcategories || []).flatMap((s) => s.products);
  return [...direct, ...fromSubs];
}

